def q(qid, chapter, topic, kind, question, explanation, options=None, answers=None, blanks=None):
    item = {
        "id": qid,
        "chapter": chapter,
        "topic": topic,
        "kind": kind,
        "question": question,
        "explanation": explanation,
    }
    if kind in {"single", "multiple"}:
        item["options"] = options
        item["answers"] = answers
    else:
        item["blanks"] = blanks
    return item


def single(qid, chapter, topic, question, answer, distractors, explanation):
    return q(qid, chapter, topic, "single", question, explanation, [answer, *distractors], [0])


def multiple(qid, chapter, topic, question, answers, distractors, explanation):
    return q(qid, chapter, topic, "multiple", question, explanation, [*answers, *distractors], list(range(len(answers))))


def blank(qid, chapter, topic, question, blanks, explanation):
    return q(qid, chapter, topic, "blank", question, explanation, blanks=blanks)


